package com.coderslab.d_Kontrola_Przeplywu_programu;

public class Main4 {
    public static void main(String[] args) {

        int i = 1;
        int j = 0;

        for (i = 1; i <= 10; i = ++i){
            System.out.println(i);
        }

        System.out.println(" ");

        while (++j <= 10) {
            System.out.println(j);
        }

    }
}



