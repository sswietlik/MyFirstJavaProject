public class Main4 {

    public static void main(String[] args) {
        int counter = 145;
        System.out.println(counter + " : counter start status");
        System.out.println(++counter + " : counter after increment status");
        System.out.println(++counter + " : counter after increment status");
        System.out.println(--counter + " : counter after declement status");

        System.out.println(counter + " final count status : " );
    }

}
