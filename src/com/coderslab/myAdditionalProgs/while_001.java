package com.coderslab.myAdditionalProgs;

public class while_001 {
    public static void main(String[] args){
        int i = 0;
        while (i < 10){
            System.out.println("i = " + i);
            i++;   //tą linijkę można usunąć stosując zapis i++  w poleceniu sys.print
        }
    }
}
